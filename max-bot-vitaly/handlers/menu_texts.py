def get_menu_text(key: str, section: str | None = None, subsection: str | None = None, item: str | None = None) -> str:
    """Возвращает текст по ключу: сначала из Google Таблицы (если есть), иначе из hardcoded."""
    try:
        from sheets import get_menu_text_from_sheet
        if section or key:
            # key может быть "drinks:coffee:japan" -> section=drinks, subsection=coffee, item=japan
            parts = key.split(":") if key else []
            s = section or (parts[0] if len(parts) > 0 else "")
            sub = subsection or (parts[1] if len(parts) > 1 else None)
            i = item or (parts[2] if len(parts) > 2 else None)
            text = get_menu_text_from_sheet(s, sub, i)
            if text:
                return text
    except Exception:
        pass
    return MENU_TEXTS.get(key, f"Материалы по теме «{key}» — в канале.")


def parse_item_payload(payload: str) -> str | None:
    """item:drinks:coffee:japan -> drinks:coffee:japan; item:travel:route_3d -> travel:route_3d."""
    if not payload.startswith("item:"):
        return None
    return payload.replace("item:", "", 1)
